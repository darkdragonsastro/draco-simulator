// ConstellationByAbbreviation returns a constellation by its IAU abbreviation.
// Returns nil if not found.
func ConstellationByAbbreviation(abbr string) *Constellation {
	for i := range AllConstellations {
		if AllConstellations[i].Abbreviation == abbr {
			return &AllConstellations[i]
		}
	}
	return nil
}

// ConstellationByName returns a constellation by its full name (case-sensitive).
// Returns nil if not found.
func ConstellationByName(name string) *Constellation {
	for i := range AllConstellations {
		if AllConstellations[i].Name == name {
			return &AllConstellations[i]
		}
	}
	return nil
}

// AllConstellationStarHIPs returns a deduplicated list of all HIP numbers
// used across all constellation stick figures. This is useful for preloading
// the required stars from the Hipparcos catalog.
func AllConstellationStarHIPs() []int {
	seen := make(map[int]bool)
	for _, c := range AllConstellations {
		for _, line := range c.Lines {
			if len(line) == 2 {
				seen[line[0]] = true
				seen[line[1]] = true
			}
		}
	}

	result := make([]int, 0, len(seen))
	for hip := range seen {
		result = append(result, hip)
	}
	return result
}
