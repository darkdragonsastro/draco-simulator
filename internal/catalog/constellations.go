// Package catalog - constellation stick figure definitions.
//
// This file defines the stick figure line data for all 88 IAU constellations.
// Each constellation is represented by a series of line segments connecting
// stars identified by their Hipparcos (HIP) catalog numbers.
//
// The data is derived from Stellarium's constellationship.fab format (public domain)
// and represents the traditional Western constellation figures.
package catalog

// Constellation represents a constellation with its stick figure lines.
